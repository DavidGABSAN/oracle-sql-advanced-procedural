Hacemos lo mismo que antes, pero esta vez buscamos un orden descendente a través de numero_estancias, para que aparezcan primero los resultados que mayor número de estancias tienen.

C)

SELECT *
FROM numero_estudiantes_tutor
ORDER BY numero_estudiantes DESC;

Aquí aplicamos la misma lógica que en la vista anterior, pero esta vez ordenamos en función de numero_estudiantes, para que aparezcan en primer lugar los tutores con mayor número de estudiantes asignados.

